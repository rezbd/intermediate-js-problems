/**
 * Double Equal:
 * only checks value
 * doesn't check type
 */
/**
 * Triple Equal:
 * checks value and type
 */
//Case 1: true is compared with 1, false is compared with 0
const first = 0;
const second = false;
if (first == second) {
	console.log("condition is true");
} else {
	console.log("condition is false");
}
